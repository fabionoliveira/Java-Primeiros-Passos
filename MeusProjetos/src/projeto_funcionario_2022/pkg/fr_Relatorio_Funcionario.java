public void MontaRelatorio_Funcionario(ArrayList<Tab_Funcionario> lista_tab_funcionario) {

        String msg = "Relatório Funcionario\n";

        for (int i = 0; i < lista_tab_funcionario.size(); i++) {

            msg = msg + "\nCódigo.....................:  " + lista_tab_funcionario.get(i).getCod_Funcionario();
            msg = msg + "\nNome.......................:  " + lista_tab_funcionario.get(i).getNm_Funcionario().toUpperCase();
            msg = msg + "\nData Nascimento..: " + lista_tab_funcionario.get(i).getDt_Nascimento().toUpperCase();
            msg = msg + "\nRG............................:  " + lista_tab_funcionario.get(i).getRg();
            msg = msg + "\nCPF..........................:  " + lista_tab_funcionario.get(i).getCpf();
            msg = msg + "\nEndereço................:  " + lista_tab_funcionario.get(i).getDs_Endereco();
            msg = msg + "\nCidade....................:  " + lista_tab_funcionario.get(i).getDs_Cidade();
            msg = msg + "\nGenero....................:  " + lista_tab_funcionario.get(i).getDs_Genero();
            msg = msg + "\nDepartamento.......:  " + lista_tab_funcionario.get(i).getDs_Departamento();
            msg = msg + "\nCargo......................:  " + lista_tab_funcionario.get(i).getDs_Cargo();
            
            msg = msg + "\n---------------------------------------------------------------";

        }
        
        ta_Relatorio_Funcionario.setText(msg);

    }


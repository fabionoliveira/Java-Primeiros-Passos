public void MontaRelatorio_Departamento(ArrayList<Tab_Departamento> lista_tab_departamento) {

        String msg = "Relatório Departamento\n";

        for (int i = 0; i < lista_tab_departamento.size(); i++) {

            msg = msg + "\nCódigo.......:  " + lista_tab_departamento.get(i).getCod_Departamento();
            msg = msg + "\nNome.........:  " + lista_tab_departamento.get(i).getNm_Departamento().toUpperCase();
            msg = msg + "\nSigla...........:  " + lista_tab_departamento.get(i).getSi_Departamento().toUpperCase();
            //msg = msg + "\nSalario........:  " + lista_tab_departamento.get(i).getSalario_Departamento();
            msg = msg + "\n----------------------------------------------------";

        }
        
        ta_Relatorio_Departamento.setText(msg);

    }



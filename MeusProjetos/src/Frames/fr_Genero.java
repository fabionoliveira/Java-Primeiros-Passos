    private boolean ConsisteCampos() {

        boolean saida = true;
        String msg = "";

        if ((!tf_Descricao.getText().trim().isEmpty())
                && (!tf_Abreviatura.getText().trim().isEmpty())) {

            linha = new Genero();

            msg = linha.is_ds_GeneroValido(tf_Descricao.getText().trim());
            if (!msg.isEmpty()) {
                JOptionPane.showMessageDialog(null, msg, "Erro", JOptionPane.ERROR_MESSAGE);
                saida = false;
            }

            msg = linha.is_abr_GeneroValido(tf_Abreviatura.getText().trim());
            if (!msg.isEmpty()) {
                JOptionPane.showMessageDialog(null, msg, "Erro", JOptionPane.ERROR_MESSAGE);
                saida = false;

            }

            if (saida) {
                linha.setDs_genero(tf_Descricao.getText().trim());
                linha.setAbr_Genero(tf_Abreviatura.getText().trim());

                if (operacao.equals("A")) {
                    linha.setCd_genero(Integer.parseInt(tf_Codigo.getText().trim()));
                }
            }

        } else {
            saida = false;
        }

        return saida;
    }

    private void Limpar() {
        tf_Abreviatura.setText(null);
        tf_Codigo.setText(null);
        tf_Descricao.setText(null);
        Habilitar(false);
        operacao = "";
    }

    private void Habilitar(boolean arg) {
        tf_Abreviatura.setEnabled(arg);
        tf_Codigo.setEnabled(arg);
        tf_Descricao.setEnabled(arg);
        //operacao = "";
    }

    private void MontaTela(int cod) {

        linha = new Genero();
        try {
            linha = dao.Consulta_Genero_Unitaria(cod);

            tf_Codigo.setText(String.valueOf(linha.getCd_genero()));
            tf_Descricao.setText(linha.getDs_genero());
            tf_Abreviatura.setText(linha.getAbr_Genero());

        } catch (Exception erro) {
            JOptionPane.showMessageDialog(null, "Erro na Montagem tela - Acesso a BD\n" + erro.getMessage(), "ERRO", JOptionPane.ERROR_MESSAGE);
        }

    }

    private void Exclusao(int cod) {

        try {
            boolean saida = dao.exclui_Genero(cod);
            if (saida) {
                JOptionPane.showMessageDialog(null, "Exclusão Concluída com sucesso!", "Mensagem", JOptionPane.ERROR_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(null, "Exclusão não Concluída. Verifique.", "Mensagem", JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception erro) {
            JOptionPane.showMessageDialog(null, "Erro na Exclusão - Acesso a BD\n" + erro.getMessage(), "ERRO", JOptionPane.ERROR_MESSAGE);
        }
    }


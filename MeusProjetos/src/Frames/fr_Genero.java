    private void tf_CodigoFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_tf_CodigoFocusLost

        try {
            int Codigo = Integer.parseInt((tf_Codigo.getText().trim()));

            MontaTela(Codigo);

            if (operacao.equals("E")) {
                int resp = JOptionPane.showConfirmDialog(null, "Confirma Exclusão?", "Confirmação", JOptionPane.YES_NO_CANCEL_OPTION);

                if (resp == 0) {
                    Exclusao(Codigo);
                }
                Limpar();
            }

            if (operacao.equals("A")) {
                Habilitar(true);
                tf_Codigo.setEnabled(false);

            }

        } catch (NumberFormatException err) {
            JOptionPane.showMessageDialog(null, "Informe o Código Corretamente", "Erro", JOptionPane.ERROR_MESSAGE);
            Limpar();
        }


    }//GEN-LAST:event_tf_CodigoFocusLost

